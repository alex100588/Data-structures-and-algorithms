function booooo(n){
    for(let i=0; i<n.length; i++){
        console.log('Booo');
    }
}

booooo([1,2,3,4,5]);


function arrayOfNTymes(n){
    let hiArray = []
    for (let i = 0; i < n; i++) {
        hiArray[i] = 'hi'
    }
    return hiArray
}

console.log(arrayOfNTymes(6));



